object Day3 {
  implicit class CoordinateMethods(coord: (Int, Int)) {
    val (x, y) = coord

    def -(that: (Int, Int)): (Int, Int) = {
      val (tX, tY) = that
      (x - tX, y - tY)
    }

    def +(that: (Int, Int)): (Int, Int) = {
      val (tX, tY) = that
      (x + tX, y + tY)
    }
  }

  val zero = (0, 0)
  val unit = (1, 1)

  case class Patch(topLeft: (Int, Int), dims: (Int, Int)) {
    val (x, y) = topLeft
    val (w, h) = dims

    val topRight: (Int, Int) = (x + w, y)
    val bottomLeft: (Int, Int) = (x, y + h)
    val bottomRight: (Int, Int) = (x + w, y + h)

    def intersects(that: Patch): Boolean =
      this.intersection(that).isDefined

    def intersection(that: Patch): Option[Patch] = {
      val iTopLeft = (Math.max(this.x, that.x), Math.max(this.y, that.y))
      val (ablx, ably) = this.bottomRight
      val (bblx, bbly) = that.bottomRight
      val iBottomRight = (Math.min(ablx, bblx), Math.min(ably, bbly))
      if (iTopLeft.x < iBottomRight.x && iTopLeft.y < iBottomRight.y) {
        Some(Patch(iTopLeft, iBottomRight - iTopLeft))
      } else {
        None
      }
    }

    def contains(c: (Int, Int)): Boolean = {
      val (x, y) = c
      return x < this.x + this.w && x > this.x && y < this.y + this.h && y > this.y
    }

    override def toString() = s"$x,$y: ${w}x${h}"
  }

